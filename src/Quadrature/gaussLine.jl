function getQuadratureLine(order::Int64)
    n::Int64 = Int64(ceil((order+1.0)/2.0))
    n1 =[2.0 0.0]
    n2 = [1.0000000000000000	-0.5773502691896257;
    1.0000000000000000	0.5773502691896257]
    n3 = [0.8888888888888888	0.0000000000000000;
    0.5555555555555556	-0.7745966692414834;
    0.5555555555555556	0.7745966692414834]
    n4 = [0.6521451548625461	-0.3399810435848563;
    0.6521451548625461	0.3399810435848563;
    0.3478548451374538	-0.8611363115940526;
    0.3478548451374538	0.8611363115940526]
    n5 = [0.5688888888888889	0.0000000000000000;
    0.4786286704993665	-0.5384693101056831;
    0.4786286704993665	0.5384693101056831;
    0.2369268850561891	-0.9061798459386640;
    0.2369268850561891	0.9061798459386640]
    n6 = [0.3607615730481386	0.6612093864662645;
    0.3607615730481386	-0.6612093864662645;
    0.4679139345726910	-0.2386191860831969;
    0.4679139345726910	0.2386191860831969;
    0.1713244923791704	-0.9324695142031521;
    0.1713244923791704	0.9324695142031521]
    if n == 1
        toReturn = n1
    elseif n==2
        toReturn = n2
    elseif n==3
        toReturn = n3
    elseif n==4
        toReturn = n4
    elseif n==5
        toReturn = n5
    elseif n==6
        toReturn = n6
    end
    w::Array{Float64} = toReturn[:,1]
    ip::Array{Float64} = toReturn[:,2]
    return w, ip
end
